// Convert tuples to game JSON format
export function buildTalents() {
  const out = {};
  for (const t of allTalents) {
    const [id, name, description, grade, effect, condition, exclude, status, exclusive] = t;
    const entry = { id: String(id), name, description, grade };
    if (effect && Object.keys(effect).length) entry.effect = effect;
    if (condition) entry.condition = condition;
    if (exclude && exclude.length) entry.exclude = exclude.map(String);
    if (status) entry.status = status;
    if (exclusive) entry.exclusive = true;
    out[String(id)] = entry;
  }
  return out;
}
